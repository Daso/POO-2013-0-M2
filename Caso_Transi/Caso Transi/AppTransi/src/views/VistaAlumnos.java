 private void mostrarAlumnos(){
 tableModel.setRowCount(transi.getAlumnos().size());
 for(int i=0; i<transi.getAlumnos().size(); i++){
 Alumno alumno = transi.getAlumnos().get(i);
 tableModel.setValueAt(alumno.getCodigo(), i, 0);
 tableModel.setValueAt(alumno.getNombre(), i, 1);
 tableModel.setValueAt(alumno.getModalidad(), i, 2);
 tableModel.setValueAt(alumno.getCalificacion().calcularPromedioFinal(), i, 3);
 }
 }

 private void mostrarEstadisticas(){
txtPFMayor.setText(String.valueOf(transi.calcularPromedioFinalMayor()));
 txtPFMenor.setText(String.valueOf(transi.calcularPromedioFinalMenor()));
 txtPromSalon.setText(String.valueOf(transi.calcularPromedioSalon()));
 }

private void mostrarCalificacion(){
int filaSeleccionada = tblAlumnos.getSelectedRow();
 if (filaSeleccionada != -1){
 JDialog vistaCalificacion = null;
 String codigoAlumno = tableModel.getValueAt(filaSeleccionada, 0).toString();
 String modalidad = tableModel.getValueAt(filaSeleccionada, 2).toString();
 if (modalidad.equals("Pr치ctica")){
 vistaCalificacion = new VistaCalificacionPractica(this, true, transi, codigoAlumno);
 }
 else{
 vistaCalificacion = new VistaCalificacionTradicional(this, true, transi, codigoAlumno);
 }
 vistaCalificacion.setVisible(true);
 vistaCalificacion.dispose();
 mostrarAlumnos();
 mostrarEstadisticas();
 }
 }


 



