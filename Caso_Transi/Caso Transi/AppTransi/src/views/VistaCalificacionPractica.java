 private void mostrarCalificacion(String codigoAlumno){
 Alumno alumno = transi.buscarAlumno(codigoAlumno);
 txtCodigo.setText(codigoAlumno);
txtNombre.setText(alumno.getNombre());
txtParcial.setText(String.valueOf(alumno.getCalificacion().getExamenParcial()));
 txtFinal.setText(String.valueOf(alumno.getCalificacion().getExamenFinal()));
txtTB1.setText(String.valueOf(((CalificacionPractica)alumno.getCalificacion() ).getTrabajo1()));
txtTB2.setText(String.valueOf(((CalificacionPractica)alumno.getCalificacion() ).getTrabajo2()));
 txtProyecto.setText(String.valueOf(((CalificacionPractica)alumno.getCalificacion() ).getProyecto()));
}
 private void actualizarCalificacion(String codigoAlumno){
double examenParcial = Double.parseDouble(txtParcial.getText());
 double examenFinal = Double.parseDouble(txtFinal.getText());
double trabajo1 = Double.parseDouble(txtTB1.getText());
 double trabajo2 = Double.parseDouble(txtTB2.getText());
 double proyecto = Double.parseDouble(txtProyecto.getText());

 transi.registrarCalificacionPractica(codigoAlumno, examenParcial, examenFinal, trabajo1, trabajo2, proyecto);
 }

